package facade;

/**
 * This class represents the monitor component in the house.
 */
public class Curtain {
  /**
   * This is the constructor for curtains.
   */
  public Curtain(){}

  /**
   * This method opens the curtains.
   */
  public void  open() {
    System.out.println("Curtain is open");
  }

  /**
   * This method closes the curtains.
   */
  public void close() {
    System.out.println("Curtain is closed");
  }
}
