package facade;

/**
 * This class represents the monitor component in the house.
 */
public class Light {
  /**
   * Constructor for Light class.
   */
  public Light(){}

  /**
   * This method turns on the lights.
   */
  public void on() {
    System.out.println("Light is on");
  }

  /**
   * This method turns off the lights.
   */
  public void off() {
    System.out.println("Light is off");
  }
}
