package facade;

/**
 * This class represents the monitor component in the house.
 */
public class Monitor {
  /**
   * This is the constructor for monitor.
   */
  public Monitor(){}

  /**
   * This method turns on the monitor.
   */
  public void on() {
    System.out.println("Monitor is on");
  }

  /**
   * This method turns off the monitor.
   */
  public void off() {
    System.out.println("Monitor is off");
  }

  /**
   * This method displays family photo on the monitor.
   */
  public void displayFamilyPhoto(){
    System.out.println("\nFamily photos");
  }


}
